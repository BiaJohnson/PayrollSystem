    private void fetchEmployeeRecord(){
        try(Connection connection = DriverManager.getConnection("jdbc:sqlite:.//database//Payroll.sqlite")){
            String query = "SELECT * FROM Employees";
            try(PreparedStatement statement = connection.prepareStatement(query)){
                ResultSet resultSet = statement.executeQuery();
                
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("id");
                model.addColumn("Name");
                model.addColumn("Department");
                model.addColumn("Salary");
                model.addColumn("Loan");
                model.addColumn("Deduction");
                model.addColumn("deduction Count");
                model.addColumn("loan Balance");
                model.addColumn("Net Salary");
                
                while(resultSet.next()){
                    String id = resultSet.getString("id");
                    String name = resultSet.getString("name");
                    String department = resultSet.getString("department");
                    String salary = resultSet.getString("salary");
                    String loan = resultSet.getString("loan");
                    String deduction = resultSet.getString("deduction");
                    String deductionCount = resultSet.getString("deduction_count");
                    String loanBal = resultSet.getString("loan_bal");
                    String netSalary = resultSet.getString("net_salary");
                    
                    model.addRow(new Object[]{id, name, department, salary, loan, deduction, deductionCount, loanBal, netSalary});
                    
                }
                
                tblEmployees.setModel(model);
            }
        }catch(SQLException e){
        e.printStackTrace();

    }
    }
        

