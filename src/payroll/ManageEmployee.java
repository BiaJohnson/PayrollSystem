    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         int x = JOptionPane.showConfirmDialog(null, "Are you sure that you want to update this record?", "Update record", JOptionPane.YES_NO_OPTION);
         if(x==0){
             String name = txtName.getText();
             String department = cmbDepartment.getSelectedItem().toString();
             String salary = txtSalary.getText();
             String loan = txtLoan.getText();
             String deduction = txtDeduction.getText();
             String deductionCount = txtDeductionCount.getText();
             String loanBalance = txtLoanBalance.getText();
             String netSalary = txtNetSalary.getText();
             
             String id = txtId.getText();
             
             String sql = "UPDATE Employees set name='"+name+"', department='"+department+"', salary='"+salary+"', loan= '"+loan+"', deduction = '"+deduction+"', deduction_count='"+deductionCount+"', loan_bal='"+loanBalance+"', net_salary='"+netSalary+"' WHERE id='"+id+"'";
             
             try(Connection connection = DriverManager.getConnection("jdbc:sqlite:.//database//Payroll.sqlite")){
                 PreparedStatement statement = connection.prepareStatement(sql);
                 statement.execute();
                 fetchEmployeeRecord();
                 JOptionPane.showMessageDialog(null, "Record Updated successfully!");
                 
             }catch(SQLException e){
                 JOptionPane.showMessageDialog(null, "Record not Updated");
             }
         }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int x = JOptionPane.showConfirmDialog(null, "Are you sure that you want to delete this record?", "Delete record", JOptionPane.YES_NO_OPTION);
        if(x==0){
            try(Connection connection = DriverManager.getConnection("jdbc:sqlite:.//database//Payroll.sqlite")){
                String idInput = txtId.getText();
                String deleteQuery = "DELETE FROM EMployees WHERE id = ?";
                
                PreparedStatement statement = connection.prepareStatement(deleteQuery);
                statement.setString(1,idInput);
                
                int rowAffected = statement.executeUpdate();
                
                if(rowAffected > 0){
                    fetchEmployeeRecord();
                    JOptionPane.showMessageDialog(null, "Record deleted successfully!");
                    clearInputs();
                }else{
                    JOptionPane.showMessageDialog(null, "There is no record associated to this Id!");
                }
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
 
