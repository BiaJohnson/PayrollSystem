    private void txtDeductionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDeductionKeyReleased
        // TODO add your handling code here:
        String salary = txtSalary.getText();
        String netSalary = txtNetSalary.getText();
        String loan = txtLoan.getText();
        String deduction = txtDeduction.getText();
        
        double salaryD = Double.parseDouble(salary);
        double loanD = Double.parseDouble(loan);
        double deductionD = Double.parseDouble(deduction);
        
        double deductions = loanD / deductionD;
        
        double netSalaryD = salaryD - deductionD;
        
        txtNetSalary.setText(String.valueOf(netSalaryD));
        txtDeductionCount.setText(String.valueOf(deductions));
        txtLoanBalance.setText(String.valueOf(loanD));
        
    }//GEN-LAST:event_txtDeductionKeyReleased

