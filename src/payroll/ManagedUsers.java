    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int x = JOptionPane.showConfirmDialog(null, "Are you sure that you want to update this record?", "Update record", JOptionPane.YES_NO_OPTION);
        if(x == 0){
        
        String id = txtId.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String role = (String) cmbRole.getSelectedItem();
        
        try(Connection connection = DriverManager.getConnection("jdbc:sqlite:.//database//Payroll.sqlite")){
            String query = "UPDATE users SET username = ?, password = ?, role = ? WHERE id = ?";
            try(PreparedStatement statement = connection.prepareStatement(query)){
                statement.setString(1,username);
                statement.setString(2,password);
                statement.setString(3,role);
                statement.setString(4,id);
                
                int rowsUpdated = statement.executeUpdate();
                
                if(rowsUpdated > 0){
                    JOptionPane.showMessageDialog(this, "User record updated sucessfully");
                    displayUserList();
                }else{
                    JOptionPane.showMessageDialog(this, "No record was updated");
                }
            }
        }catch(SQLException e){
            e.printStackTrace();
        }
      }
    }//GEN-LAST:event_btnEditActionPerformed

