    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int y = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete", "Delete Record", JOptionPane.YES_NO_OPTION);
        if (y == 0){
            try(Connection connection = DriverManager.getConnection("jdbc:sqlite:.//database//Payroll.sqlite")){
                String idInput = txtId.getText();
                String deleteQuery = "DELETE FROM users WHERE id = ?";
                try{
                    PreparedStatement statement = connection.prepareStatement(deleteQuery);
                    statement.setString(1, idInput);
                    
                    int rowsAffected = statement.executeUpdate();
                    
                    if(rowsAffected > 0){
                        JOptionPane.showMessageDialog(null, "Record Deleted sucessfully!");
                        displayUserList();
                    }else{
                        JOptionPane.showMessageDialog(null, "Record not Deleted!");
                    }
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }catch(SQLException e){
                
            }
        }
            
    }//GEN-LAST:event_btnDeleteActionPerformed

